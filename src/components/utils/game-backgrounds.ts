export const getGameGradient = (gameName: string): string => {
  // Return specific gradients based on game
  switch (gameName) {
    case "BGMI":
    case "PUBG Mobile":
    case "BGMI / PUBG Mobile":
      return "from-gray-800/30 to-black/30";
    case "Free Fire":
      return "from-gray-800/30 to-black/30";
    case "COD Mobile":
    case "Call of Duty Mobile":
      return "from-gray-800/30 to-black/30";
    case "Valorant":
    case "VALORANT Mobile":
      return "from-red-500/30 via-blue-500/20 to-cyan-500/30";
    case "Clash Royale":
      return "from-purple-500/30 via-blue-500/20 to-indigo-500/30";
    case "Brawl Stars":
      return "from-yellow-500/30 via-orange-500/20 to-red-500/30";
    case "Apex Legends Mobile":
      return "from-orange-500/30 via-red-500/20 to-purple-500/30";
    case "League of Legends: Wild Rift":
      return "from-blue-500/30 via-gold-500/20 to-cyan-500/30";
    default:
      return "from-primary/20 via-accent/10 to-tertiary/20";
  }
};